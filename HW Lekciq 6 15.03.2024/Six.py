#Да се напише програма, която да принтира всички числа от 0 до 6, без да
#включва 3 и 6.
#Изход: 0 1 2 4 5

def main():
    for i in range(7):  # Обхождаме числата от 0 до 6
         if i == 3 or i == 6:  # Пропускаме числата 3 и 6
            continue
    print(i, end=" ")  # Принтираме текущото число

if __name__ == "__main__":
    main()

