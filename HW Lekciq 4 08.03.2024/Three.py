#Напишете програма, която намира най-голямото по стойност число, измежду три
#дадени числа. 

# Въвеждане на три числа от потребителя

num1 = float(input('Въведете първото число: '))
num2 = float(input('Въведете второто число: '))
num3 = float(input('Въведете третото число: '))

# Намиране на най-голямото число
max_number = num1

if num2>max_number:
    max_number = num2

if num3>max_number:
    max_number=num3

# Извеждане на най-голямото число
print('Най-голямото число е: ', max_number)